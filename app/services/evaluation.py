
import logging
from typing import Any

from datasets import Dataset
from langchain_openai import ChatOpenAI
from langchain_community.embeddings import HuggingFaceEmbeddings

from app.core.config import settings
from app.models.schemas import EvaluationResult

from ragas import evaluate
from ragas.llms import LangchainLLMWrapper
from ragas.embeddings import LangchainEmbeddingsWrapper
from ragas.metrics import (
        answer_relevancy,
        context_precision,
        faithfulness,
    )

logger = logging.getLogger(__name__)

def _get_evaluator_llm() -> LangchainLLMWrapper:    
    langchain_llm = ChatOpenAI(
        api_key=settings.GROQ_API_KEY,
        base_url="https://api.groq.com/openai/v1",
        model="llama-3.3-70b-versatile",
        temperature=0.0,  
    )
    return LangchainLLMWrapper(langchain_llm)

def _get_evaluator_embeddings() -> LangchainEmbeddingsWrapper:
    """Configure the local embedding model for Answer Relevancy checks."""
    langchain_embeddings = HuggingFaceEmbeddings(
        model_name=settings.EMBEDDING_MODEL,
        model_kwargs={"device": settings.EMBEDDING_DEVICE},
    )
    return LangchainEmbeddingsWrapper(langchain_embeddings)

async def run_evaluation_batch(
    questions: list[str],
    contexts_list: list[list[str]],
    answers: list[str],
    ground_truths: list[str] | None = None,
) -> EvaluationResult:
    """
    Run Ragas evaluation on a batch of queries.
    
    Args:
        questions: The original user queries.
        contexts_list: A list of lists, where each sublist contains the chunks retrieved for a query.
        answers: The synthesized answers generated by the pipeline.
        ground_truths: The expected golden answers.
    """
    logger.info("Starting offline Ragas evaluation for %d queries", len(questions))
    
    data: dict[str, list[Any]] = {
        "question": questions,
        "answer": answers,
        "contexts": contexts_list,
    }
    
    if ground_truths:
        data["ground_truth"] = ground_truths
    
    hf_dataset = Dataset.from_dict(data)
    
    evaluator_llm = _get_evaluator_llm()
    evaluator_embeddings = _get_evaluator_embeddings()
    
    metrics = [faithfulness, answer_relevancy, context_precision]
    for metric in metrics:
        metric.llm = evaluator_llm
        if hasattr(metric, "embeddings"):
            metric.embeddings = evaluator_embeddings
    
    try:        
        import asyncio
        loop = asyncio.get_running_loop()
        result = await loop.run_in_executor(
            None,
            lambda: evaluate(
                dataset=hf_dataset,
                metrics=metrics,
                llm=evaluator_llm,
                embeddings=evaluator_embeddings,
                raise_exceptions=False,
            )
        )        
        
        scores = result.scores.to_pandas().mean().to_dict()
        f_score = scores.get("faithfulness", 0.0)
        c_score = scores.get("context_precision", 0.0)
        a_score = scores.get("answer_relevancy", 0.0)
        
        overall = (f_score + c_score + a_score) / 3.0

        eval_res = EvaluationResult(
            context_relevance=round(c_score, 4),
            faithfulness=round(f_score, 4),
            answer_relevance=round(a_score, 4),
            overall_score=round(overall, 4),
        )
        
        logger.info("Evaluation complete: Overall Score = %.2f", eval_res.overall_score)
        return eval_res

    except Exception as exc:
        logger.error("Ragas evaluation failed: %s", exc, exc_info=True)
        raise